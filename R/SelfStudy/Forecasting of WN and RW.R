library(forecast)
model.noise<-auto.arima(white.noise,ic="aic",trace=T,stepwise=T,approximation=F)
#as result, ARIMA(0,0,0) was selected as the best model.                          


plot(forecast(model.noise, h=100),flwd=5)
abline(h=mean(white.noise))
